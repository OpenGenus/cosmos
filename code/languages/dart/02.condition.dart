void main() {
  // we're setting the value of x
  int x = 3;
  
  // if else condition
  // checking if else is 10 or not
  if(x==10) {
    print('x is 10');
  }
  // checking if x is 5 or not 
  else if(x==5) {
    print('x is 5');
  }
  // if none of the above is true, then
  // this code will run
  else  {
    print('x is not 10 or 5');
  }
}