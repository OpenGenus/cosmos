for _ in range(int(input())):
    n = int(input())
    s = 0
    if n <= 250000:
        s = 0
    elif n >= 250001 and n <= 500000:
        s = 0.05*(n - 250000)
    elif n > 500000 and n <= 750000:
        s = 12500 + 0.1*(n - 500000)
    elif n > 750000 and n <= 1000000:
        s = 12500 + 25000 + 0.15*(n-750000)
    elif n >= 1000001 and n <= 1250000:
        s = 12500 + 25000 + 37500 + 0.2*(n-1000000)
    elif n >= 1250001 and n <= 1500000:
        s = 12500 + 25000 + 37500 + 50000 + 0.25*(n-1250000)
    else:
        s = 12500 + 25000 + 37500 + 50000 + 62500 + 0.3*(n-1500000)
    print(int(n-s))
