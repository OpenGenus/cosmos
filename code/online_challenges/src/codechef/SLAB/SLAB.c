#include<stdio.h>
int main()
{
	int t;
	scanf("%d",&t);
	while(t--)
	{
		long long int y,tax=0;
		scanf("%lld",&y);
		if(y<=250000)
		tax=0;
		if(y>250000&&y<=500000)
		tax=0.05*(y-250000);
		else if(y>500000&&y<=750000)
		tax=0.05*(500000-250000)+0.1*(y-500000);
		else if(y>750000&&y<=1000000)
		tax=0.05*(500000-250000)+0.1*(750000-500000)+0.15*(y-750000);
		else if(y>1000000&&y<=1250000)
		tax=0.05*(500000-250000)+0.1*(750000-500000)+0.15*(1000000-750000)+0.2*(y-1000000);
		else if(y>1250000&&y<=1500000)
		tax=0.05*(500000-250000)+0.1*(750000-500000)+0.15*(1000000-750000)+0.2*(1250000-1000000)+0.25*(y-1250000);
		else if(y>1500000)
		tax=0.05*(500000-250000)+0.1*(750000-500000)+0.15*(1000000-750000)+0.2*(1250000-1000000)+0.25*(1500000-1250000)+0.3*(y-1500000);
		printf("%lld\n",y-tax);

	}
}